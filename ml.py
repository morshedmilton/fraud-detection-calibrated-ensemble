# Make sure y is a clean pandas Series
y_dev = pd.Series(y_dev).astype(int).reset_index(drop=True)
y_test = pd.Series(y_test).astype(int).reset_index(drop=True)

# Make sure X is a DataFrame
if not isinstance(X_dev, pd.DataFrame):
    raise TypeError("X_dev must be a pandas DataFrame from Step 1.")
