def clip_probs(p):
    p = np.asarray(p, dtype=float)
    return np.clip(p, 1e-6, 1 - 1e-6)

def calculate_metrics(y_true, y_prob, threshold=0.50):
    y_true = np.asarray(y_true, dtype=int)
    y_prob = clip_probs(y_prob)
    y_pred = (y_prob >= threshold).astype(int)

    metrics = {
        "AUPRC": average_precision_score(y_true, y_prob),
        "Recall": recall_score(y_true, y_pred, zero_division=0),
        "Precision": precision_score(y_true, y_pred, zero_division=0),
        "F1": f1_score(y_true, y_pred, zero_division=0),
        "F2": fbeta_score(y_true, y_pred, beta=2, zero_division=0),
        "ROC-AUC": roc_auc_score(y_true, y_prob),
        "MCC": matthews_corrcoef(y_true, y_pred),
        "Brier": brier_score_loss(y_true, y_prob),
        "LogLoss": log_loss(y_true, y_prob, labels=[0, 1]),
        "Predicted Fraud Alerts": int(y_pred.sum())
    }
    return metrics, y_pred

def fit_platt(prob_train, y_train):
    model = LogisticRegression(
        max_iter=1000,
        solver="lbfgs",
        random_state=RANDOM_STATE
    )
    model.fit(np.asarray(prob_train).reshape(-1, 1), np.asarray(y_train))
    return model

def predict_platt(model, prob):
    return model.predict_proba(np.asarray(prob).reshape(-1, 1))[:, 1]

def fit_isotonic(prob_train, y_train):
    model = IsotonicRegression(
        out_of_bounds="clip",
        y_min=0.0,
        y_max=1.0
    )
    model.fit(np.asarray(prob_train), np.asarray(y_train))
    return model

def predict_isotonic(model, prob):
    return model.predict(np.asarray(prob))

def crossfit_calibration(dev_prob, test_prob, y_true, method):
    dev_prob = clip_probs(dev_prob)
    test_prob = clip_probs(test_prob)

    if method == "Raw":
        return {
            "dev_prob": dev_prob,
            "test_prob": test_prob,
            "full_model": None
        }

    calib_cv = StratifiedKFold(
        n_splits=CALIBRATION_SPLITS,
        shuffle=True,
        random_state=RANDOM_STATE
    )

    oof_calibrated = np.zeros(len(y_true), dtype=float)

    for fold, (train_idx, valid_idx) in enumerate(calib_cv.split(dev_prob.reshape(-1, 1), y_true), start=1):
        fold_prob_train = dev_prob[train_idx]
        fold_prob_valid = dev_prob[valid_idx]
        fold_y_train = y_true.iloc[train_idx]

        if method == "Platt":
            fold_model = fit_platt(fold_prob_train, fold_y_train)
            oof_calibrated[valid_idx] = predict_platt(fold_model, fold_prob_valid)

        elif method == "Isotonic":
            fold_model = fit_isotonic(fold_prob_train, fold_y_train)
            oof_calibrated[valid_idx] = predict_isotonic(fold_model, fold_prob_valid)

    # Fit once on full development probabilities for later use on final test
    if method == "Platt":
        full_model = fit_platt(dev_prob, y_true)
        test_calibrated = predict_platt(full_model, test_prob)

    elif method == "Isotonic":
        full_model = fit_isotonic(dev_prob, y_true)
        test_calibrated = predict_isotonic(full_model, test_prob)

    return {
        "dev_prob": clip_probs(oof_calibrated),
        "test_prob": clip_probs(test_calibrated),
        "full_model": full_model
    }

def choose_best_calibration(dev_prob, test_prob, y_true, model_name):
    method_outputs = {}
    method_rows = []

    for method in CALIBRATION_METHODS:
        out = crossfit_calibration(dev_prob, test_prob, y_true, method)
        method_outputs[method] = out

        method_rows.append({
            "Model": model_name,
            "Calibration": method,
            "Dev_Brier": brier_score_loss(y_true, out["dev_prob"]),
            "Dev_LogLoss": log_loss(y_true, out["dev_prob"], labels=[0, 1]),
            "Dev_AUPRC": average_precision_score(y_true, out["dev_prob"]),
            "Dev_ROC-AUC": roc_auc_score(y_true, out["dev_prob"])
        })

    method_df = pd.DataFrame(method_rows).sort_values(
        by=["Dev_Brier", "Dev_LogLoss", "Dev_AUPRC"],
        ascending=[True, True, False]
    ).reset_index(drop=True)

    # Step 4 is explicitly a calibration step.
    # So we keep "Raw" as the no-calibration baseline,
    # but choose the selected calibration only from Platt vs Isotonic.
    selectable_df = method_df[method_df["Calibration"] != "Raw"].copy()
    selectable_df = selectable_df.sort_values(
        by=["Dev_Brier", "Dev_LogLoss", "Dev_AUPRC"],
        ascending=[True, True, False]
    ).reset_index(drop=True)

    chosen_method = selectable_df.iloc[0]["Calibration"]

    return {
        "comparison_df": method_df,
        "chosen_method": chosen_method,
        "chosen_dev_prob": method_outputs[chosen_method]["dev_prob"],
        "chosen_test_prob": method_outputs[chosen_method]["test_prob"],
        "chosen_calibrator": method_outputs[chosen_method]["full_model"],
        "all_method_outputs": method_outputs
    }

def optimize_threshold(y_true, y_prob):
    rows = []

    for threshold in THRESHOLD_SCAN:
        y_pred = (y_prob >= threshold).astype(int)

        rows.append({
            "Threshold": float(threshold),
            "Recall": recall_score(y_true, y_pred, zero_division=0),
            "Precision": precision_score(y_true, y_pred, zero_division=0),
            "F1": f1_score(y_true, y_pred, zero_division=0),
            "F2": fbeta_score(y_true, y_pred, beta=2, zero_division=0),
            "MCC": matthews_corrcoef(y_true, y_pred),
            "Predicted Fraud Alerts": int(y_pred.sum())
        })

    threshold_df = pd.DataFrame(rows)

    # Main criterion: highest F2
    best_f2 = threshold_df["F2"].max()
    best_rows = threshold_df[threshold_df["F2"] == best_f2].copy()

    # Tie-breakers: higher MCC, then higher Precision, then lower threshold
    best_rows = best_rows.sort_values(
        by=["MCC", "Precision", "Threshold"],
        ascending=[False, False, True]
    ).reset_index(drop=True)

    best_threshold = float(best_rows.iloc[0]["Threshold"])

    return best_threshold, threshold_df

def summarize_single_setting(y_true, y_prob, threshold, label):
    metrics, _ = calculate_metrics(y_true, y_prob, threshold=threshold)
    metrics["Setting"] = label
    metrics["Threshold"] = float(threshold)
    return metrics

