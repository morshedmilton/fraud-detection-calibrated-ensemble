def clip_probs(p):
    p = np.asarray(p, dtype=float)
    return np.clip(p, 1e-6, 1 - 1e-6)

def anomaly_score_from_pipeline(pipe, X_df):
    return -pipe.decision_function(X_df)

def to_ordered_dataframe(X_like):
    if isinstance(X_like, pd.DataFrame):
        X_df = X_like.copy()
    else:
        X_df = pd.DataFrame(X_like, columns=X_dev.columns)

    X_df = X_df.loc[:, X_dev.columns]
    return X_df

def get_base_probabilities(X_df):
    X_df = to_ordered_dataframe(X_df)

    lr_prob = fitted_baseline_models["Weighted Logistic Regression"].predict_proba(X_df)[:, 1]
    rf_prob = fitted_baseline_models["Random Forest"].predict_proba(X_df)[:, 1]
    xgb_prob = fitted_baseline_models["XGBoost"].predict_proba(X_df)[:, 1]

    out = pd.DataFrame({
        "lr_prob": lr_prob,
        "rf_prob": rf_prob,
        "xgb_prob": xgb_prob
    })
    return out

def get_raw_final_scores(X_df):
    X_df = to_ordered_dataframe(X_df)
    base_meta = get_base_probabilities(X_df)

    if final_model_name == "Soft Voting (Supervised)":
        raw_prob = base_meta.mean(axis=1).to_numpy()

    elif final_model_name == "Supervised Stacking":
        raw_prob = final_stack_supervised.predict_proba(base_meta)[:, 1]

    elif final_model_name == "Hybrid Stacking + IsolationForest":
        hybrid_meta = base_meta.copy()
        hybrid_meta["iforest_anomaly_score"] = anomaly_score_from_pipeline(final_iforest_pipe, X_df)
        raw_prob = final_stack_hybrid.predict_proba(hybrid_meta)[:, 1]

    else:
        raise ValueError(f"Unknown final model name: {final_model_name}")

    return clip_probs(raw_prob)

def apply_selected_calibration(raw_prob):
    raw_prob = clip_probs(raw_prob)

    if final_calibration == "Raw":
        calibrated = raw_prob

    elif final_calibration == "Platt":
        calibrated = chosen_calibrator.predict_proba(raw_prob.reshape(-1, 1))[:, 1]

    elif final_calibration == "Isotonic":
        calibrated = chosen_calibrator.predict(raw_prob)

    else:
        raise ValueError(f"Unknown calibration method: {final_calibration}")

    return clip_probs(calibrated)

def predict_final_proba(X_like):
    X_df = to_ordered_dataframe(X_like)
    raw_prob = get_raw_final_scores(X_df)
    calibrated_prob = apply_selected_calibration(raw_prob)
    return np.column_stack([1 - calibrated_prob, calibrated_prob])

