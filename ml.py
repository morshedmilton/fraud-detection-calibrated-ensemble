def build_iforest_pipeline(contamination_value):
    contamination_value = float(max(contamination_value, 1e-4))

    return Pipeline([
        ("prep", clone(preprocessor)),
        ("model", IsolationForest(
            n_estimators=300,
            contamination=contamination_value,
            random_state=RANDOM_STATE,
            n_jobs=-1
        ))
    ])


def anomaly_score_from_pipeline(pipe, X):
    # IsolationForest.decision_function:
    # higher value = more normal
    # lower value = more abnormal
    # We flip the sign so that:
    # higher score = more suspicious / more anomalous
    return -pipe.decision_function(X)


def build_meta_learner():
    return Pipeline([
        ("scaler", StandardScaler()),
        ("model", LogisticRegression(
            class_weight="balanced",
            max_iter=1000,
            solver="liblinear",
            random_state=RANDOM_STATE
        ))
    ])


def get_oof_stack_predictions(X_meta, y_true, cv_object, label):
    oof_prob = np.zeros(len(y_true), dtype=float)

    for fold, (train_idx, valid_idx) in enumerate(cv_object.split(X_meta, y_true), start=1):
        meta_pipe = build_meta_learner()
        meta_pipe.fit(X_meta.iloc[train_idx], y_true.iloc[train_idx])
        oof_prob[valid_idx] = meta_pipe.predict_proba(X_meta.iloc[valid_idx])[:, 1]
        print(f"{label} meta fold {fold}/5 done")

    final_meta_pipe = build_meta_learner()
    final_meta_pipe.fit(X_meta, y_true)

    return oof_prob, final_meta_pipe

