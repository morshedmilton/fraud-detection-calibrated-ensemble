def choose_case(indices, probs, mode="highest"):
    if len(indices) == 0:
        return None

    candidate_probs = probs[indices]

    if mode == "highest":
        chosen_pos = int(np.argmax(candidate_probs))
    elif mode == "lowest":
        chosen_pos = int(np.argmin(candidate_probs))
    else:
        raise ValueError("mode must be 'highest' or 'lowest'")

    return int(indices[chosen_pos])

